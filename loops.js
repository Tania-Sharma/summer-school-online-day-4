
console.log(" Loops")
console.log("(1 to 10) by FOR loop:");
for (let a = 1; a <= 10; a++) {
  console.log(a);
}


console.log("(even numbers 2 to 20) by While loop :");
let b = 2;
while (b <= 20) {
      console.log(b);
      b= b+2;
}


console.log("(10 down to 1) by Do-while loop:");
let c = 10;
do {
      console.log(c);
      c--;
} while (c >= 1);
